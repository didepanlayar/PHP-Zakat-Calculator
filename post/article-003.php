                    <div class="post-paragraph">
                        <p>Maal berasal dari kata bahasa Arab artinya harta atau kekayaan (al-amwal, jamak dari kata maal) adalah “segala hal yang diinginkan manusia untuk disimpan dan dimiliki” (Lisan ul-Arab). Menurut Islam sendiri, harta merupakan sesuatu yang boleh atau dapat dimiliki dan digunakan (dimanfaatkan) sesuai kebutuhannya.</p>
                        <p>Oleh karena itu dalam pengertiannya, zakat maal berarti zakat yang dikenakan atas segala jenis harta, yang secara zat maupun substansi perolehannya tidak bertentangan dengan ketentuan agama.</p>
                        <p>Sebagai contoh, zakat maal terdiri atas simpanan kekayaan seperti uang, emas, surat berharga, penghasilan profesi, aset perdagangan, hasil barang tambang atau hasil laut, hasil sewa aset dan lain sebagainya.</p>
                        <p>Sebagaimana yang dijelaskan oleh Syaikh Dr. Yusuf Al-Qardhawi dalam kitabnya Fiqh uz-Zakah, zakat maal meliputi:</p>
                        <ol>
                            <li>Zakat simpanan emas, perak, dan barang berharga lainnya;</li>
                            <li>Zakat atas aset perdagangan;</li>
                            <li>Zakat atas hewan ternak;</li>
                            <li>Zakat atas hasil pertanian;</li>
                            <li>Zakat atas hasil olahan tanaman dan hewan;</li>
                            <li>Zakat atas hasil tambang dan tangkapan laut;</li>
                            <li>Zakat atas hasil penyewaan asset;</li>
                            <li>Zakat atas hasil jasa profesi;</li>
                            <li>Zakat atas hasil saham dan obligasi.</li>
                        </ol>
                        <p>Begitupun dengan yang dijelaskan di dalam UU No. 23 Tahun 2011, zakat maal meliputi:</p>
                        <ol>
                            <li>Emas, perak, dan logam mulia lainnya;</li>
                            <li>Uang dan surat berharga lainnya;</li>
                            <li>Perniagaan;</li>
                            <li>Pertanian, perkebunan, dan kehutanan;</li>
                            <li>Peternakan dan perikanan;</li>
                            <li>Pertambangan;</li>
                            <li>Perindustrian;</li>
                            <li>Pendapatan dan jasa; dan</li>
                            <li>Rikaz</li>
                        </ol>
                        <p>Adapun syarat harta yang terkena kewajiban zakat maal yaitu sebagai berikut:</p>
                        <ol>
                            <li>Kepemilikan penuh</li>
                            <li>Harta halal dan diperoleh secara halal</li>
                            <li>Harta yang dapat berkembang atau diproduktifkan (dimanfaatkan)</li>
                            <li>Mencukupi nishab</li>
                            <li>Bebas dari hutang</li>
                            <li>Mencapai haul</li>
                            <li>Atau dapat ditunaikan saat panen</li>
                        </ol>
