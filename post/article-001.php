                        <p>Apa itu <strong>zakat</strong>? Sahabat, masih ada yang bingung tentang apa itu zakat ? Zakat dalam bahasa Arab: زكاة transliterasi: Zakah yang dari segi bahasa <strong>mengandung beberapa makna</strong> yakni,</p>
                        <p><strong>Pertama,</strong> zakat bermakna <strong>At-Thohuru</strong>, yang artinya <strong>membersihkan</strong> atau <strong>menyucikan</strong> yakni orang yang selalu menunaikan zakat karena Allah akan membersihkan dan menyucikan baik hartanya maupun jiwanya</p>
                        <p><strong>Kedua,</strong> zakat bermakna <strong>Al-Barakatu</strong>, yang artinya berkah maksudnya harta yang dizakati akan selalu <strong>dilimpahkan keberkahan</strong> oleh Allah SWT, kemudian keberkahan harta ini akan berdampak kepada keberkahan hidup</p>
                        <p><strong>Ketiga,</strong> zakat bermakna <strong>An-Numuw</strong>, yang artinya tumbuh dan berkembang. Makna ini menegaskan bahwa orang yang selalu menunaikan zakat, hartanya akan selalu terus <strong>tumbuh</strong> dan <strong>berkembang</strong> Insya Allah</p>
                        <p>Dan <strong>keempat</strong> zakat bermakna <strong>As-Sholahu</strong>, yang artinya beres atau keberesan, yaitu bahwa orang-orang yang selalu menunaikan zakat, hartanya akan selalu <strong>beres</strong> dan <strong>jauh dari masalah</strong>.</p>
                        <h3>8 Golongan Penerima Zakat</h3>
                        <p>Sedangkan dalam segi istilah adalah harta tertentu yang wajib dikeluarkan oleh orang yang beragama Islam dan diberikan kepada golongan yang berhak menerimanya (yang dikenal dengan sebutan 8 ashnaf yakni:</p>
                        <ol>
                            <li>Fakir</li>
                            <li>Miskin</li>
