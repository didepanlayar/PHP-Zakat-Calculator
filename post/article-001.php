                            <li>Ibnu Sabil</li>
                        </ol>
                        <h3>Jenis Zakat</h3>
                        <p>Sesuai dengan jenisnya, zakat terbagi menjadi 2 (dua) jenis yaitu,</p>
                        <ol>
                            <li><strong>Zakat Mal</strong>: zakat harta yang dikeluarkan setiap satu tahun sekali sesuai dengan nisab dan haulnya.</li>
                            <li><strong>Zakat Fitrah</strong>: zakat yang dikeluarkan pada saat bulan Ramadhan yang dibatasi sebelum shalat Idul Fitri.</li>
