                        <p>Zakat penghasilan atau yang dikenal juga sebagai zakat profesi; zakat pendapatan adalah bagian dari zakat mal yang wajib dikeluarkan atas harta yang berasal dari pendapatan / penghasilan rutin dari pekerjaan yang tidak melanggar syariah. Nishab zakat penghasilan sebesar 85 gram emas per tahun. Kadar zakat penghasilan senilai 2,5%.</p>
                        <p>Fatwa Majelis Ulama Indonesia (MUI) menjelaskan, penghasilan yang dimaksud ialah setiap pendapatan seperti gaji, honorarium, upah, jasa, dan lain-lainnya yang diperoleh dengan cara halal, baik rutin seperti pejabat negara, pegawai, karyawan, maupun tidak rutin seperti dokter, pengacara, konsultan, dan sejenisnya, serta pendapatan yang diperoleh dari pekerjaan bebas lainnya.</p>
                        <p>Dalam praktiknya, zakat penghasilan dapat ditunaikan setiap bulan dengan nilai nishab perbulannya adalah setara dengan nilai seperduabelas dari 85 gram emas (mengikuti harga <i>Buy Back</i> emas pada hari dimana zakat akan ditunaikan), dengan kadar 2,5%. Jadi apabila penghasilan setiap bulan telah melebihi nilai nishab bulanan, maka wajib dikeluarkan zakatnya sebesar 2,5% dari penghasilannya tersebut.</p>
                        <p>Ada banyak jenis profesi dengan pembayaran rutin maupun tidak, dengan penghasilan sama dan tidak dalam setiap bulannya. Jika penghasilan dalam 1 bulan tidak mencapai nishab, maka hasil pendapatan selama 1 tahun dikumpulkan atau dihitung, kemudian zakat ditunaikan jika penghasilan bersihnya sudah cukup nishab.</p>
