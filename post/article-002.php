                        <p>Cara menghitung Zakat Penghasilan:</p>
                        <h4 class="text-center">2,5% x Jumlah penghasilan dalam 1 bulan</h4>
                        <p>Contoh:<br>Jika harga emas pada hari ini sebesar Rp800.000/gram, maka nishab zakat penghasilan dalam satu tahun adalah Rp68.000.000,-. Penghasilan Bapak Fulan sebesar Rp10.000.000/ bulan, atau Rp120.000.000,- dalam satu tahun. Artinya penghasilan Bapak Fulan sudah wajib zakat. Maka zakat Bapak Fulan adalah Rp250.000,-/ bulan.</p>
